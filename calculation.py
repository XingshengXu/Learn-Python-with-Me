# Addition function
def add(a, b):
    return a + b

# Subtraction function


def subtract(a, b):
    return a - b

# Multiplication function


def multiply(a, b):
    return a * b

# Division function


def divide(a, b):
    if b != 0:
        return a / b
    else:
        return "Cannot divide by zero"


