# 赋值运算
x = 5
x += 3  # x = x + 3
x -= 3  # x = x - 3
x *= 3  # x = x * 3
x /= 3  # x = x / 3
x **= 3  # x = x ** 3
x %= 3  # x = x % 3
x //= 3  # x = x // 3
print(x)

